import os
import json
import pandas as pd
import numpy as np
import torch
import torch.nn as nn
from torch.utils.data import DataLoader, TensorDataset
from sklearn.preprocessing import StandardScaler

# Replicate the CNN architecture to load the model
class BKI_CNN(nn.Module):
    def __init__(self):
        super(BKI_CNN, self).__init__()
        self.conv_blocks = nn.Sequential(
            nn.Conv1d(3, 32, kernel_size=5, padding=2),
            nn.BatchNorm1d(32),
            nn.ReLU(),
            nn.MaxPool1d(2),
            
            nn.Conv1d(32, 64, kernel_size=5, padding=2),
            nn.BatchNorm1d(64),
            nn.ReLU(),
            nn.MaxPool1d(2),
            
            nn.Conv1d(64, 128, kernel_size=3, padding=1),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.AdaptiveAvgPool1d(1)
        )
        self.fc = nn.Sequential(
            nn.Linear(128, 64),
            nn.Dropout(0.3),
            nn.ReLU(),
            nn.Linear(64, 1)
        )
        
    def forward(self, x):
        features = self.conv_blocks(x).squeeze(2)
        return self.fc(features)

def create_windows_per_patient(df, patient_ids, features, window_size=100, stride=20):
    X_windows = []
    y_labels = []
    
    for pid in patient_ids:
        pdf = df[df['patient_id'] == pid]
        if pdf.empty:
            continue
        X = pdf[features].to_numpy()
        y = pdf['in_hospital_death'].iloc[0]
        
        for i in range(0, len(X) - window_size + 1, stride):
            X_windows.append(X[i:i+window_size])
            y_labels.append(y)
            
    return np.array(X_windows, dtype=np.float32), np.array(y_labels, dtype=np.float32)

def generate_cot_rationale(predicted_prob, actual_label, avg_flow, avg_pao, avg_pes):
    """
    Generates a synthetic chain-of-thought rationale explaining the prediction.
    In a real scenario, this could be generated by an un-quantized LLM teacher.
    """
    prediction = 1 if predicted_prob >= 0.5 else 0
    
    rationale = "Chain-of-Thought Analysis:\n"
    rationale += f"1. Observe telemetry: Average Flow = {avg_flow:.2f} l/s, Pao = {avg_pao:.2f} cm H2O, Pes = {avg_pes:.2f} cm H2O.\n"
    
    if prediction == 1 and actual_label == 1:
        rationale += "2. The model correctly identified high-risk indicators (True Positive). The elevated airway pressure combined with erratic flow patterns strongly suggests deteriorating lung compliance typical of VAP.\n"
    elif prediction == 1 and actual_label == 0:
        rationale += "2. The model flagged a high risk, but the patient survived (False Positive). The model likely over-indexed on the transient pressure spikes (Pao) which were caused by temporary suctioning rather than alveolar collapse.\n"
    elif prediction == 0 and actual_label == 1:
        rationale += "2. The model failed to flag the risk (False Negative). The baseline pressures appeared stable, but the model missed the subtle, gradual decay in the Pes trajectory which hinted at systemic inflammation.\n"
    else:
        rationale += "2. The model correctly identified a stable state (True Negative). Telemetry remains within physiological tolerance with no acute deviations.\n"
        
    rationale += f"Conclusion: The predicted probability of {predicted_prob:.4f} reflects the network's mapping of these mechanical features."
    return rationale

def main():
    device = torch.device("cuda:7" if torch.cuda.is_available() else "cpu")
    print(f"Using device: {device}")
    
    # Load model
    model = BKI_CNN().to(device)
    model_path = "output_from_advanced_training/bki_classifier_advanced_cnn.pt"
    if os.path.exists(model_path):
        model.load_state_dict(torch.load(model_path, map_location=device))
        print("Loaded advanced CNN weights.")
    else:
        print("Model weights not found. Using random initialization for demonstration.")
    model.eval()
    
    # Load data
    data_files = [f"data/waveform_data/P0{i}Waveform.xlsx" for i in range(1, 8)]
    death_labels = [0, 1, 0, 0, 0, 0, 1]
    
    dfs = []
    for i, path in enumerate(data_files):
        if os.path.exists(path):
            df = pd.read_excel(path)
            df["in_hospital_death"] = death_labels[i]
            df["patient_id"] = i + 1
            dfs.append(df)
            
    if not dfs:
        print("No waveform data found. Please ensure data exists.")
        return
        
    merged_df = pd.concat(dfs, axis=0, ignore_index=True)
    features = ['Flow [l/s]', 'Pao [cm H2O]', 'Pes [cm H2O]']
    scaler = StandardScaler()
    merged_df[features] = scaler.fit_transform(merged_df[features])
    
    # Generate windows
    patient_ids = [1, 2, 3, 4, 5, 6, 7]
    X_test, y_test = create_windows_per_patient(merged_df, patient_ids, features)
    
    X_test_t = torch.tensor(X_test).transpose(1, 2)
    y_test_t = torch.tensor(y_test).unsqueeze(1)
    
    test_loader = DataLoader(TensorDataset(X_test_t, y_test_t), batch_size=512, shuffle=False)
    
    dataset_records = []
    
    print("Executing model to generate CoT dataset...")
    with torch.no_grad():
        idx = 0
        for X_batch, y_batch in test_loader:
            X_batch_dev = X_batch.to(device)
            logits = model(X_batch_dev)
            probs = torch.sigmoid(logits).cpu().numpy()
            
            for i in range(len(probs)):
                # Inverse transform just to get some realistic numbers for the prompt
                # Here we just take the mean of the window
                avg_flow = X_batch[i][0].mean().item()
                avg_pao = X_batch[i][1].mean().item()
                avg_pes = X_batch[i][2].mean().item()
                
                prob = float(probs[i][0])
                label = int(y_batch[i][0].item())
                
                prompt = (f"Analyze the following mechanical ventilation telemetry for VAP risk.\n"
                          f"Average Flow: {avg_flow:.2f}\n"
                          f"Average Pao: {avg_pao:.2f}\n"
                          f"Average Pes: {avg_pes:.2f}\n"
                          f"Model Prediction Probability: {prob:.4f}\n"
                          f"Actual Outcome: {label}")
                
                response = generate_cot_rationale(prob, label, avg_flow, avg_pao, avg_pes)
                
                dataset_records.append({
                    "text": f"<bos><start_of_turn>user\n{prompt}<end_of_turn>\n<start_of_turn>model\n{response}<end_of_turn><eos>"
                })
                idx += 1

    # Save to JSONL
    out_file = "lora_adapters/cot_dataset.jsonl"
    os.makedirs("lora_adapters", exist_ok=True)
    with open(out_file, "w", encoding="utf-8") as f:
        for rec in dataset_records:
            f.write(json.dumps(rec) + "\n")
            
    print(f"Dataset generated successfully! Saved {len(dataset_records)} records to {out_file}")

if __name__ == "__main__":
    main()
